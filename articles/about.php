<h1><u>About NICS</u></h1>

<p>NICS is a <span class="ita">web-based</span> command &amp; control environment for <span class="ita">small</span> to 
<span class="ita">large</span> to <span class="ita">extreme scale</span> incidents that 
facilitates collaboration across Federal, Tribal, Military, State, County, &amp; Local/Municipal levels of 
preparedness, planning, response, and recovery for <span class="ita">all-risk/all-hazard</span> events.  NICS facilitates situational awareness for widely dispersed responders.</p>

<p>NICS is sponsored by the <b>Department of Homeland Security Science and 
Technology Directorate</b>, and is being developed by <b>Massachusetts 
Institute of Technology Lincoln Laboratory</b> in partnership with the 
operators from the <b>California First Responder Community</b>.</p>

<p>NICS is:</p>
<ul>
<li>Based upon <span class="ita">Open standards</span>: It runs on any computer, any operating system, any browser (except Internet Explorer 8 and earlier;  IE 9 works fine)</li>
<li><span class="ita">Non-proprietary</span>: Not vendor owned; this is a 
<span class="ita">community development project</span></li>
<li>Uses an <span class="ita">"App Store" model</span>: Apps can be developed 
by anyone, with easy plug-&amp;-play functionality</li>
<li><span class="ita">Always improving</span>: Like the <span class="ita">Web</span>, NICS will never 
be done; it will always be improved and adapted by the <span class="ita">community</span></li>
<li>Available at <span class="ita">No cost</span> to Emergency Responders</li>
<li>Aimed at the <span class="ita">Tired&#8211;Dirty&#8211;Hungry</span>: 
Carefully designed for the responder under extreme stress</li>
<li>Focused on <span class="ita">scalability</span>: Technology for small to large to extreme scale incidents</li>
</ul>

<p>NICS provides collaboration and communication capabilities across all echelons of responders; it enhances the quality and accessibility of sensor data; 
and it integrates location data for resources, vehicles, and personnel. During an incident, NICS provides an information backbone that manages and distributes data, 
including real-time vehicle location feeds, weather, critical infrastructure, and terrain information.  </p>

<p>Incident data is displayed by using a web-based, open standards platform that allows users with the proper permissions to 
log into a map-based environment accessible via an ordinary web browser and Internet connection. NICS offers graphical tools, 
including geo-referenced virtual whiteboards, for dynamic interagency collaboration that facilitates a coordinated response. </p>

<p>Using these tools, responders are able to quickly form teams, send messages to one another, and remotely share maps and drawings 
that enhance the management of the incident.  As of July 2012, over 700 first responders across more than 50 organizations have been 
trained in the use of NICS and are employing NICS daily (as examples, all CAL FIRE regional units, California Emergency Management 
Agency, all CAL FIRE Incident Management Teams, Riverside County Fire and Sheriff's Departments, Riverside County Office of Emergency 
Services, Sycuan Fire Department and the Golden Eagle Hotshots (Bureau of Indian Affairs), San Marcos Fire Department, San Diego 
Fire-Rescue Department, and the U.S. Border Patrol, to cite a few.)</p>

<p>CAL FIRE units and local departments (county and municipal) in Riverside and San Diego, as well as other California Counties, 
are integrating NICS into their day-to-day operations to train personnel and respond to incidents, such as wildland fires. 
During the 2011 Southern California fire season (June to November), the system was used in operational evaluations on a number 
of large wildland fires (Riverside and San Diego) that progressed beyond initial attack.  Since 2010, NICS has been used on over 200 actual incidents.  
It has also been used in the Los Angeles, Boston, and San Diego marathons for management of medical team response.</p>

<p> NICS has seen this adoption by first responder organizations 
because:</p>
<ul>
<li>It is web-based and technology neutral</li>
<li>It does not try to do everything – It is designed for the responder under extreme stress -- the Tired-Dirty-Hungry; it is easy to learn and use. </li>
<li>It is free for responder organizations (all funding has been provided by the 
government; no vendor has proprietary rights to NICS; it is an open standards, community development project).</li>
</ul>

