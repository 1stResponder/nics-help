<p>NICS supports an apps plug-in model, including the following:</p>
<ul>
<li>The ability to organize and share whiteboard rooms</li>
<li>The ability to communicate and document information using “Text Based Chatting” (public and private)</li>
<li>Multiple base map layers including streets, topographical, satellite, and aeronautical  </li>
<li>A well-organized data warehouse</li>
<li>Automatic Vehicle Location (AVL)</li>
<li>Position Location Information (PLI) satellite based personnel tracking</li>
<li>Automated Aircraft Flight Following (AFF)</li>
<li>Robust real-time weather feeds</li>
<li>Form and Reports</li>
</ul>

