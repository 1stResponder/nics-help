<h1><u>First Steps In NICS</u></h1>

<p>Welcome to the Next-Generation Incident Command System (NICS). 
Before we get started, please take the time to familiarize yourself 
with the following important capabilities available any time you are logged into NICS.</p>

<h3><span class="bu">Update User Profile</span></h3>

<p>Each user has their own user profile that will contain contact and user information. 
Please keep it up to date. To access this window, click on your name in the top right 
corner of the window, and select  <i>'Account Information'</i> from the drop down menu.</p>
<p>From this window, you can set your name, title, rank, etc., as well as contact information.  
You may select the User Contact Info tab to update your phone number, radio, and other contact 
information.  Press SUBMIT to save the changes.</p>

