    <td>What types of devices does the mobile application run on?</td>
    <td>Currently the application only runs on Android. An iOS version is in development</td>
  </tr>
  <tr>
    <td>What is the minimum version of the Android OS that is supported</td>
    <td>Gingerbread is the minimum version (v 2.3.x)</td>
  </tr>
  <tr>
    <td>Does it matter if I have a tablet?</td>
    <td>No, the application runs on phones or tablets</td>
  </tr>
  <tr>
    <td>I'm having problems connecting, my login/password is being rejected</td>
    <td>Please check that you can login via the Web UI at nics.ll.mit.edu, you should also check to see if you are trying to log into the correct workspace (training or incident)</td>
  </tr>
  <tr>
    <td>I want to select a different server before I log in</td>
    <td>Select the wrench icon from the top left of the login screen and select a different server option</td>
  </tr>
  <tr>
    <td>How do I know what version of the application I'm using?</td>
    <td>You can either check under settings&gt;application manager&gt;NICS and look at the top or you can ensure that the application has exited by using the application manager to force-stop the application (or restart your device). The splash screen will briefly show you the version.</td>
  </tr>
  <tr>
    <td>Can I run SARapp and NICS at the same time?</td>
    <td>Yes</td>
  </tr>
  <tr>
    <td>I don't want to be tracked when I'm using the mobile application</td>
    <td>Under the application settings un-check GPS mobile device tracking</td>
  </tr>
  <tr>
    <td>There is a blue dot on my map?</td>
    <td>That is your location as your device GPS is currently reporting it. You can verify this against Google Maps or another mapping application</td>
  </tr>
  <tr>
    <td>My install is being blocked, what do I do?</td>
    <td>Please ensure that you have enabled the installation of 3rd party applications on your phone. You can find this under your phone's settings. In addition other apps running in the background may filter or block the installation. Please disable those before trying again.</td>
  </tr>
