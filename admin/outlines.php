<h2>Data Layers</h2>
<p>Separating information out into data layers allows users to recall 
information on demand as it is needed.</p>
<p>Data Layers window, describe base (overall look and feel) and data 
(overlaid information)difference. Range from static (boundaries) to 
live (AVL) feeds. For example, look at fire history for CA. If it is 
the first time, loading may take a few seconds. Data layers are 
powerful and can be expanded on.</p>

<h2>Locking the Room</h2>
<p>The information on the map is valuable, so you want to make sure it 
is not accidentally altered.</p>
<p>Draw, lock the room, try to draw, unlock the room, draw.</p>

<h2>Chat</h2>
