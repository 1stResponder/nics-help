<h1>Video Outlines</h1>

<h2>Incidents and Rooms</h2>
<p>NICS uses a 2 level structure to organize its workspaces between 
users. When you first log into NICS, you are not a member of any 
workspaces.</p>
<p>To join one, open the incidents menu from the main 
bar, and choose one from the drop down list. Notice how the incident 
name appears in the bar, indicating your membership. Each incident 
has several workspaces, known as rooms, to further organize each 
incident. Joining a room is similar to joining an incident, just 
choose from the drop down menu on the left. You may also create your 
own room or incident by selecting the option in their respective 
menus. Just fill out the dialog and hit enter.</p>
<p>When you enter a room, it appears as a tab in the rooms bar, after 
any other rooms you are currently subscribed to. A personal “My Map” 
room is independent of incidents; it is always available for your use. 
Use it to draft a sketch before sharing it with others</p>

<h2>Windows and Panels</h2>
<p>Your workspace is valuable, and you may want to clear out panels
that you are not using. For the map area, you may close open windows 
by clicking on the x in the top right corner. You may also hide 
all windows by selecting the option in the tools menu. These may be 
shown again by selecting the show all windows button. To clear up 
the top bar, you can leave rooms by clicking on the x next to their
name. Finally, you can also hide and show the side and chat panels by 
clicking on their respective bars.</p>

<h2>Main Toolbar</h2>
<p>Most manipulations in NICS can be accessed from the main toolbar. 
Drag/Select similar, but can pan map with drag</p>
